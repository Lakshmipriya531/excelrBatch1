public class Demo014 {
        public static void main(String[] args) {
            int num = 5;
    
            
            System.out.println("Unary plus: " + (+num)); // Output: 5
    
            
            System.out.println("Unary minus: " + (-num)); // Output: -5
    
            
            System.out.println("Prefix increment: " + (++num)); // Output: 6
    
            
            System.out.println("Postfix increment: " + (num++)); // Output: 6
            System.out.println("After postfix increment: " + num); // Output: 7
    
            
            System.out.println("Prefix decrement: " + (--num)); // Output: 6
    
            
            System.out.println("Postfix decrement: " + (num--)); // Output: 6
            System.out.println("After postfix decrement: " + num); // Output: 5
    
            
            boolean flag = true;
            System.out.println("Logical NOT: " + (!flag)); // Output: false
        }
    }
    
    
    

